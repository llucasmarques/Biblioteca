    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        setarValores atribuir = new setarValores();
        atribuir.setNome(txtNome.getText());
        atribuir.setCpf(txtCpf.getText());
        atribuir.setData1(txtData_nsc.getText());
        atribuir.setData2_end(txtEndereco.getText());
        atribuir.setEmail(txtEmail.getText());
        atribuir.setTel(txtCelular.getText());
        atribuir.setData3_S(jComboSexo.getSelectedItem().toString());
        jButton2ActionPerformed(evt);
        
        JOptionPane.showMessageDialog(null, "Cliente Cadastrado!");
        
        try {
            atribuir.setClientes();
        } catch (ParseException | SQLException ex) {
            Logger.getLogger(TelaCadCliente.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

