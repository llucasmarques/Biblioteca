    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        cliente cli = new cliente();
        ClienteConfig cf = new ClienteConfig();
        List<cliente> lista = new ArrayList<cliente>();
        try {
            lista = cf.getDados2(cpf.getText());
            int a = cf.getID2(cpf.getText());
            if(a != -1){
                for (cliente cl:lista){
                Nome.setText(cl.getNome());
                Email.setText(cl.getEmail());
                Endereco.setText(cl.getEndereco());
                txtcelular.setText(cl.getTelefone());
                txtCpfMostra.setText(cl.getCpf());
                txtData.setText(cf.converteDatas(cl.getData_nsc()));
             
                }
            }else{
                JOptionPane.showMessageDialog(null, "Informe CPF valido");
            }
            
        } catch (SQLException | ParseException ex) {
            Logger.getLogger(TelaBuscaCli.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        setarValores atribuir = new setarValores();
        ClienteConfig cc = new ClienteConfig();

        atribuir.setNome(Nome.getText());
        atribuir.setCpf(txtCpfMostra.getText());
        atribuir.setData1(txtData.getText());
        atribuir.setData2_end(Endereco.getText());
        atribuir.setEmail(Email.getText());
        atribuir.setTel(txtcelular.getText());
        atribuir.setData3_S(boxSexo.getSelectedItem().toString());
        
        JOptionPane.showMessageDialog(null, "Dados Alterados");
        
        Nome.setText("");
        txtCpfMostra.setText("");
        txtData.setText("");
        Endereco.setText("");
        Email.setText("");
        txtcelular.setText("");
        
        try {
            int up = cc.getID2(cpf.getText());
            cpf.setText("");
            atribuir.upClientes(up);
        } catch (ParseException | SQLException ex) {
            Logger.getLogger(TelaAlteraCli.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed


