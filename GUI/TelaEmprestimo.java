    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ClienteConfig cc = new ClienteConfig();
        setarValores add = new setarValores();
        LivroConfig lc = new LivroConfig();
        try {
            int a = cc.getID2(txtCpf.getText());
            int b = Integer.parseInt(txtId.getText());
            int c = lc.pegaQTD(b);
            
            String r = lc.verifica(b);
            if((a != -1) && (r != "-") && (c != -1)){
                add.setId(cc.getID2(txtCpf.getText()));
                add.setId_livro(Integer.parseInt(txtId.getText()));
                add.setData_prevista(txtDataPrev.getText());
                add.setQtd(Integer.parseInt(jComboQtd.getSelectedItem().toString()));
                add.setSt(1);
                add.setarEmpresa();
                JOptionPane.showMessageDialog(null, "Emprestimo realizado com Sucesso!");
                txtCpf.setText("");
                txtId.setText("");
                txtDataPrev.setText("");
            }else if((a == -1 ) || (r == "-")){
                JOptionPane.showMessageDialog(null, "CPF ou Livro Invalido");
                txtCpf.setText("");
                txtId.setText("");
            }else{
                JOptionPane.showMessageDialog(null, "Nao ha livros Suficiente");
            }
            
            
            
            } catch (SQLException | ParseException ex) {
                Logger.getLogger(TelaEmprestimo.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed


