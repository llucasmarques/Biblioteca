    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        cliente cli = new cliente();
        ClienteConfig cf = new ClienteConfig();
        List<cliente> lista = new ArrayList<cliente>();
        try {
            lista = cf.getDados2(txtCpf.getText());
            int a = cf.getID2(txtCpf.getText());
            if(a != -1){
                for (cliente cl:lista){
                txtNome.setText(cl.getNome());
                txtSexo.setText(cl.getSexo());
                txtEmail.setText(cl.getEmail());
                txtEndereco.setText(cl.getEndereco());
                txtcelular.setText(cl.getTelefone());
                txtCpfMostra.setText(cl.getCpf());
                txtData.setText(cf.converteDatas(cl.getData_nsc()));
                txtDebito.setText(cl.getDebito().toString()+" R$");
                txtCpf.setText("");
             
                }
            }else{
                JOptionPane.showMessageDialog(null, "Informe CPF valido");
            }
            
        } catch (SQLException | ParseException ex) {
            Logger.getLogger(TelaBuscaCli.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

